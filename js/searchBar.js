// Shopping List
let shoppingList = [];
function displayList(){
    let array = document.cookie.split("; ");
    console.log(array)
    var myOBJ = new Object;
    for (var i = 0; i < array.length; i++) {
        var key = array[i].substring(0,array[i].indexOf("="));
        var value = array[i].substring(array[i].indexOf("=") + 1, array[i].length);
        if (key == 'shoeName') {
            myOBJ.shoeName = value;
        }else if (key == 'price') {
            myOBJ.price = value;
        }else if (key == 'picture1'){
            myOBJ.picture1 = value;
        }
    }
    shoppingList.push(myOBJ);
    console.log(shoppingList)
    let htmldisplayheader = shoppingList.map((item) =>{
        return`
        <li>
            <div class="name">
                <div class="title">${item.shoeName}</div>
                <div class="price">${item.price}</div>
            </div>
            <div class="picture"><img src="${item.picture1}" alt=""></div>
            <div id="remove"><i class="fa fa-trash" aria-hidden="true" onclick="deleteItem(${item.shoeName})"></i></div>
        </li>`
    })
    document.getElementById("ShoppingBag").innerHTML = htmldisplayheader.join("");
}

